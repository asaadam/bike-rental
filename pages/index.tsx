export async function getServerSideProps(context: GetServerSidePropsContext) {
  const cookies = new Cookies(context.req.headers.cookie);
  if (cookies.get('isLoggedIn') && cookies.get('role') === 'MANAGER') {
    return {
      redirect: {
        permanent: false,
        destination: '/dashboard',
      },
    };
  }
  return {
    props: {},
  };
}
